<?php
// কোডটি আমাদের কাছ থেকে দুটি সংখ্যা ইনপুট নিয়ে তাদের মধ্যে ভাগশেষ (remainder) নির্ণয় করে।
$input = trim(fgets(STDIN)); //  ইনপুট গ্রহণ ও ইনপুটের শুরু বা শেষে থাকা অতিরিক্ত ফাঁকা স্থান বা নতুন লাইন (\n) সরিয়ে দেয়।

// এখানে explode() ফাংশন একটি স্ট্রিংকে নির্দিষ্ট বিভাজক (' ', অর্থাৎ ফাঁকা স্থান) ব্যবহার করে আলাদা অংশে ভাগ করে। আলাদা করা অংশগুলো $a এবং $b তে সংরক্ষণ করা হয়।
list($a, $b) = explode(' ', $input);

// এখানে $a এবং $b-এর মানগুলো স্ট্রিং হিসাবে ছিল, যেগুলোকে পূর্ণসংখ্যা (integer) তে রূপান্তর করা
$a = (int)$a;
$b = (int)$b;

// ভাগশেষ নির্ণয় এবং আউটপুট
echo $a % $b;
?>