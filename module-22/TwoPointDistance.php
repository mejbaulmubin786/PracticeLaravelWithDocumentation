<?php
    // ব্যবহারকারীর কাছ থেকে  দুটি বিন্দর কো-অরডিনেট বা স্থানাঙ্ক নেওয়া হচ্ছে। প্রথম "%f" হলো x-coordinate, এবং দ্বিতীয় "%f" হলো y-coordinate
     fscanf(STDIN, "%f %f", $x1, $y1);
	 fscanf(STDIN, "%f %f", $x2, $y2);
	 
	//  এখানে  "%f" নির্দেশ করে ভাসমান বিন্দু সংখ্যা (decimal number) ইনপুট হবে।  
	
	$distance = sqrt(pow($x2 - $x1, 2) + pow($y2 - $y1, 2));
	
	//  এখানে  $x2 - $x1: x-অক্ষের পার্থক্য।   এখানে আমরা স্থানাঙ্ক নির্ণয়ের সূত্র ব্যবহার করেছি।
	
	//  এখানে  $x2 - $x1: x-অক্ষের পার্থক্য।  
	//  এখানে  $y2 - $y1: y-অক্ষের পার্থক্য। 
	//  এখানে  pow($value, 2): মানের বর্গ (square)।  
	//  এখানে  sqrt(...): স্কোয়ার রুট (square root) নির্ণয় করে।  
    
    printf("Distance: %.2f\n", $distance);
?>
